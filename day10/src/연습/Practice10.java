package 연습;

public class Practice10 {
	public static void main(String[] args) {

		int[][] map = new int[5][5];
		
		for(int i=0; i<map.length; i++) {
			for(int k=0; k<map[i].length; k++) {
				System.out.println(map[i][k]);
				System.out.println("\t");
			}
			System.out.println();
		}
				
			
	}
}