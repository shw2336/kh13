package loop3;

public class Test01반복문 {
	public static void main(String[] args) {
		//두 번째 반복문 - while 반복문
		
		//1부터 10까지 출력
		for(int i=1; i<=10; i++) {
			System.out.println(i);
		}
		
		System.out.println();
		
		int n=1;
		while(n <=10) {
			System.out.println(n);
			n++;
		}
	}
}
